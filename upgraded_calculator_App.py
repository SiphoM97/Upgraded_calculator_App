Num1 = float(input("enter number here"))
op = input("enter operator here")
Num2 = float(input("Enter number here"))

if op == "+":
    print(Num1 + Num2)
elif op == "-":
    print(Num1 - Num2)
elif op == "*":
    print(Num1 * Num2)
elif op == "/":
    print(Num1 / Num2)
else :
    print("invalid error")